package com.cy.po;

public class Mall {
	
	private int mallid;
	private String name;
	private int carcategory;
	private String caridstr;
	/**
	 * @return the mallid
	 */
	public int getMallid() {
		return mallid;
	}
	/**
	 * @param mallid the mallid to set
	 */
	public void setMallid(int mallid) {
		this.mallid = mallid;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the carcategory
	 */
	public int getCarcategory() {
		return carcategory;
	}
	/**
	 * @param carcategory the carcategory to set
	 */
	public void setCarcategory(int carcategory) {
		this.carcategory = carcategory;
	}
	/**
	 * @return the caridstr
	 */
	public String getCaridstr() {
		return caridstr;
	}
	/**
	 * @param caridstr the caridstr to set
	 */
	public void setCaridstr(String caridstr) {
		this.caridstr = caridstr;
	}

}
