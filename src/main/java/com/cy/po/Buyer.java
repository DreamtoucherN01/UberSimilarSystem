package com.cy.po;

public class Buyer {
	
	private int id;
	private String name;
	private int shoppingcartid;
	private String info;
	private String boughtcarid;
	private String password;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the shoppingcartid
	 */
	public int getShoppingcartid() {
		return shoppingcartid;
	}
	/**
	 * @param shoppingcartid the shoppingcartid to set
	 */
	public void setShoppingcartid(int shoppingcartid) {
		this.shoppingcartid = shoppingcartid;
	}
	/**
	 * @return the info
	 */
	public String getInfo() {
		return info;
	}
	/**
	 * @param info the info to set
	 */
	public void setInfo(String info) {
		this.info = info;
	}
	/**
	 * @return the boughtcarid
	 */
	public String getBoughtcarid() {
		return boughtcarid;
	}
	/**
	 * @param boughtcarid the boughtcarid to set
	 */
	public void setBoughtcarid(String boughtcarid) {
		this.boughtcarid = boughtcarid;
	}
	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}
	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}
	
	

}
