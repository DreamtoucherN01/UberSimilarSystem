package com.cy.po;

public class Data{
	private int no;
	private String name;
	private int total;
	private int nimei;
	/**
	 * @return the no
	 */
	public int getNo() {
		return no;
	}
	/**
	 * @param no the no to set
	 */
	public void setNo(int no) {
		this.no = no;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the total
	 */
	public int getTotal() {
		return total;
	}
	/**
	 * @param total the total to set
	 */
	public void setTotal(int total) {
		this.total = total;
	}
	/**
	 * @return the nimei
	 */
	public int getNimei() {
		return nimei;
	}
	/**
	 * @param nimei the nimei to set
	 */
	public void setNimei(int nimei) {
		this.nimei = nimei;
	}
}