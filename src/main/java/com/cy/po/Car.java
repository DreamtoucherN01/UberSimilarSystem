package com.cy.po;

public class Car {
	private int carid;
	private String name;
	private int number;
	
	/**
	 * @return the carid
	 */
	public int getCarid() {
		return carid;
	}
	/**
	 * @param carid the carid to set
	 */
	public void setCarid(int carid) {
		this.carid = carid;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the number
	 */
	public int getNumber() {
		return number;
	}
	/**
	 * @param number the number to set
	 */
	public void setNumber(int number) {
		this.number = number;
	}
	

}
